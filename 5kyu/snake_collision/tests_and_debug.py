def print_field(field, snake):
  # field must already be lists format
    copy = field.copy()
    a = list(zip(snake[0], snake[1]))
    for y, x in a:
        copy[y][x] = 'o'
    print('\n'.join(''.join(u) for u in copy) + '\n')

